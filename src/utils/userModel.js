import mongoose from "mongoose";
// creating user schema
const user = new mongoose.Schema(
  {
    userusername: {
      type: String,
      required: true,
      unique: true,
      min: 3,
      max: 20,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    img: {
      type: String,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
    phone: {
      type: String,
    },
    address: {
      type: String,
    },
  },
  { timestamps: true }
);
// removed the try and catch block and replaced it with this
export default mongoose.models.USER || mongoose.model("USER", user);
